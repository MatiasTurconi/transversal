    ///--------------BOTON BUSCAR--------------
    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        boolean msj=false;
        verificarSelector();
        if(jRBID.isSelected()){
            msj=verificarCampos(jTID);
            if(!msj){
                viewUniversidad.alum=viewUniversidad.alumD.buscarAlumno(Integer.parseInt(jTID.getText()));
            }
        }else if(jRBDNI.isSelected()){
            msj=verificarCampos(jTDNI);
            if(!msj){
                viewUniversidad.alum=viewUniversidad.alumD.buscarAlumnoPorDNI(Integer.parseInt(jTDNI.getText()));
            }
        }
        if(viewUniversidad.alum!=null && !msj){
            jBEliminar.setEnabled(true);
            jCBModificar.setEnabled(true);
            jTID.setText(viewUniversidad.alum.getIdAlumno()+"");
            jTDNI.setText(viewUniversidad.alum.getDni()+"");
            jTApellido.setText(viewUniversidad.alum.getApellido());
            jTNombre.setText(viewUniversidad.alum.getNombre());
            jDCNacimiento.setDate(Date.valueOf(viewUniversidad.alum.getFechaNacimiento()));
        }else if((jRBID.isSelected()||jRBDNI.isSelected())&&!msj){
            JOptionPane.showMessageDialog(null, "Alumno no encontrado");
            jCBModificar.setEnabled(false);
            Limpiar();
        }
        viewUniversidad.alum=null;
    }//GEN-LAST:event_jBBuscarActionPerformed

