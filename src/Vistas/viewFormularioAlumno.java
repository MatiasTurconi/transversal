    ///---------VERIFICAR CAMPO TEXTO REUTILIZAR CODIGO---------
    private void verificar(java.awt.event.KeyEvent evt,javax.swing.JTextField tx){
        if((evt.getKeyChar()<65||evt.getKeyChar()>90)&&(evt.getKeyChar()<97||evt.getKeyChar()>122)&&evt.getKeyChar()!=8&&evt.getKeyChar()!=32){
            JOptionPane.showMessageDialog(this, "Igresar solo letras","Advertencia",JOptionPane.WARNING_MESSAGE);
            evt.consume();
            tx.requestFocus();
        }
    }
    
    private void modificar(){
        if(jCBModificar.isSelected()){
            jRBID.setEnabled(false);
            jRBDNI.setEnabled(false);
            jTID.setEditable(false);
            jTDNI.setEditable(true);
            jDCNacimiento.setEnabled(true);
            jTNombre.setEditable(true);
            jTApellido.setEditable(true);
            jBModificar.setEnabled(true);
            jBEliminar.setEnabled(false);
            jBBuscar.setEnabled(false);
            jDCNacimiento.getDateEditor().setEnabled(false);
        }else{
            jRBID.setEnabled(true);
            jRBDNI.setEnabled(true);
            if(jRBID.isSelected()){
                jTID.setEditable(true);
                jTDNI.setEditable(false);
            }else{
                jTID.setEditable(false);
                jTDNI.setEditable(true);
            }
            jDCNacimiento.setEnabled(false);
            jTNombre.setEditable(false);
            jTApellido.setEditable(false);
            jBModificar.setEnabled(false);
            jBEliminar.setEnabled(true);
            jBBuscar.setEnabled(true);
        }
    }
    
    private void eliminar(){
        if(!verificarCampos(jTID)){
            viewUniversidad.alumD.eliminarAlumno(Integer.parseInt(jTID.getText()));
            Limpiar();
        }
    }
    
    private void verificarSelector(){
        if(!jRBID.isSelected()&&!jRBDNI.isSelected()){
            JOptionPane.showMessageDialog(null, "Elija una opcion");
        }
    }
    
    private boolean verificarCampos(javax.swing.JTextField tx){
        if(tx.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Completar Campo");
            tx.requestFocus();
            return true;
        }else{return false;}
    }
    
    private void Limpiar(){
        jTID.setText("");
        jTDNI.setText("");
        jTNombre.setText("");
        jTApellido.setText("");
        jDCNacimiento.setCalendar(null);
        jBEliminar.setEnabled(false);
        jCBModificar.setEnabled(false);
    }

    private void verificarSoloNumeros(java.awt.event.KeyEvent evt,javax.swing.JTextField tx){
        if((evt.getKeyChar()<48||evt.getKeyChar()>57)&&evt.getKeyChar()!=8){
            JOptionPane.showMessageDialog(this, "Igresar solo numeros","Advertencia",JOptionPane.WARNING_MESSAGE);
            evt.consume();
            tx.requestFocus();
        }
    }

