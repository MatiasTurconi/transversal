    private void jBInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBInscribirActionPerformed
        boolean msj=false;
        if(!msj){msj=verificarCampos(jTDNI);}
        if(!msj){msj=verificarCampos(jTNombre);}
        if(!msj){msj=verificarCampos(jTApellido);}
        if(!msj){
             if(jDCNacimiento.getDate()==null){
                msj=true;
                JOptionPane.showMessageDialog(null, "Completar Fecha");
                jDCNacimiento.requestFocus();
            }
        }
        if(!msj){
            viewUniversidad.alum=new Alumno(Integer.parseInt(jTDNI.getText()),jTNombre.getText(),jTApellido.getText(),jDCNacimiento.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate(),true);
            viewUniversidad.alumD.guardarAlumno(viewUniversidad.alum);
            viewUniversidad.alum=null;
            Limpiar();
        }
    }//GEN-LAST:event_jBInscribirActionPerformed

